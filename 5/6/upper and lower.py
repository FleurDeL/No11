#대소문자가 있는 알파벳에 적용. 숫자 특수문자 한글 등은 불가능
print("Ups AND Downs".lower()) # ups and downs
"Ups AND Downs".upper() # UPS AND DOWNS
"Ups AND Downs".swapcase() # 대문자는 소문자로, 소문자는 대문자로
                            # uPS and dOWNS
"Ups AND Downs".capitalize() # 첫번쨰 문자만 대문자로 나머지는 소문자로
                            # Ups and downs
